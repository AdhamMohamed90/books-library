    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            String name = jTextField1.getText().trim();
            String creator = jTextField2.getText().trim();
            String department = jComboBox1.getSelectedItem().toString();
            int price = Integer.parseInt(jTextField4.getText().trim());
            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection c = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "123");
            String sql = "update book set name = ?, creator = ?, department = ?, price = ? where name = ?";
            PreparedStatement s = c.prepareStatement(sql);
            s.setString(1, name);
            s.setString(2, creator);
            s.setString(3, department);
            s.setInt(4, price);
            s.setString(5, jTable1.getValueAt(row, 0).toString());
            model.setValueAt(name, row, 0);
            model.setValueAt(creator, row, 1);
            model.setValueAt(department, row, 2);
            model.setValueAt(price, row, 3);
            s.executeQuery();

            JOptionPane.showMessageDialog(null, "information updated succefully", "success", 0, new ImageIcon("OIP_2.png"));

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "invalid", 0, new ImageIcon("R.png"));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

