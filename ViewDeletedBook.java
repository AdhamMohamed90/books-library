    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
            String name = jTable1.getValueAt(row, 0).toString();
            String creator = jTable1.getValueAt(row, 1).toString();
            String Department = jTable1.getValueAt(row, 2).toString();
            int price = Integer.parseInt(jTable1.getValueAt(row, 3).toString());

            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection c = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "123");
            String sql = "insert into book values(?,?,?,?)";
            PreparedStatement s = c.prepareStatement(sql);
            s.setString(1, name);
                    s.setString(2, creator);
                    s.setString(3, Department);
                    s.setInt(4, price);
            s.executeQuery();
            model.removeRow(row);
            sql = "delete from deletedBook where name = ?";
            s = c.prepareStatement(sql);
            s.setString(1, name);
            s.executeUpdate();
            JOptionPane.showMessageDialog(null, "information updated succefully", "success", 0, new ImageIcon("OIP_2.png"));

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(),"invalid",0,new ImageIcon("R.png"));
        }
    }//GEN-LAST:event_jButton2ActionPerformed

