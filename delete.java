    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            Object ob2[] = new String[2];
            ob2[0] = "YES";
            ob2[1] = "NO";
            int yesOrNo = JOptionPane.showOptionDialog(null, "are you sure to delete this data ", "CHOISE", 0, 3, null, ob2, null);
            if (yesOrNo == 0) {
                int delete = Integer.parseInt(jTextField1.getText().trim());
                Class.forName("oracle.jdbc.driver.OracleDriver");
                Connection c = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "123");
                String get = "select * from Librarian where id = ?";

                PreparedStatement prs = c.prepareStatement(get);
                prs.setInt(1, delete);
                ResultSet rs = prs.executeQuery();
                if (rs.next()) {
                    int id = rs.getInt(1);
                    String name = rs.getString(2);
                    String password = rs.getString(3);
                    String email = rs.getString(4);
                    String address = rs.getString(5);
                    String city = rs.getString(6);
                    String num = rs.getString(7);
                    String transfer = "insert into deletedLibrarian values(?,?,?,?,?,?,?)";
                    PreparedStatement ps = c.prepareStatement(transfer);
                    ps.setInt(1, id);
                    ps.setString(2, name);
                    ps.setString(3, password);
                    ps.setString(4, email);
                    ps.setString(5, address);
                    ps.setString(6, city);
                    ps.setString(7, num);
                    ps.executeUpdate();
                    String sql = "delete from librarian where id = ?";
                    PreparedStatement s = c.prepareStatement(sql);
                    s.setInt(1, delete);
                    s.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Librarian deleted succefully","success",0,new ImageIcon("OIP_2.png"));
                } else {
                    JOptionPane.showMessageDialog(null, "Librarian not found","invalid",0,new ImageIcon("R.png"));
                }

            }
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(),"invalid",0,new ImageIcon("R.png"));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

