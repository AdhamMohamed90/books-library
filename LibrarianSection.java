    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try {

            Class.forName("oracle.jdbc.driver.OracleDriver");
            Connection c = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "123");
            String sql = "select * from issueBook ";
            PreparedStatement s = c.prepareStatement(sql);
            ResultSet rs = s.executeQuery();
            viewIssueBook v = new viewIssueBook();
            int count = 0;
            String end = "The books that needs to be returned are:\n";
            while (rs.next()) {
                count++;
                String name = rs.getString(1);
                String creator = rs.getString(2);
                String department = rs.getString(3);
                int price = rs.getInt(4);
                Date issue_date = rs.getDate(5);
                Date return_date = rs.getDate(6);
                long days_left = ChronoUnit.DAYS.between(LocalDate.now(), return_date.toLocalDate());

                Object[] add = {name, creator, department, price, issue_date, return_date, days_left};

                DefaultTableModel model = (DefaultTableModel) viewIssueBook.jTable1.getModel();
                model.addRow(add);
                if (days_left == 0) {
                    v.check = true;
                    end += name + "\n";
                }

            }
            v.text = end;
            if (count > 0) {
                this.setVisible(false);
                v.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "There are no issued books","invalid",0,new ImageIcon("R.png"));
            }

            rs.close();
            s.close();
            c.close();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(),"invalid",0,new ImageIcon("R.png"));
        }
    }//GEN-LAST:event_jButton6ActionPerformed

