    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText().trim();
        int id = Integer.parseInt(jTextField2.getText());
        char pass[] = jPasswordField1.getPassword();
        String parse = String.valueOf(pass);
        char confirmPass[] = jPasswordField1.getPassword();
        String confirmParse = String.valueOf(pass);
        if (parse.length() < 8) {
            JOptionPane.showMessageDialog(null, "Password must contain more than 7 characters","Invalid",0,  new ImageIcon("R.png"));
        } else {
            if (confirmParse.equals(parse)) {
                try {

                    Class.forName("oracle.jdbc.driver.OracleDriver");
                    Connection c = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "123");
                    String sql = "select name from librarian where name = ? and id = ?";
                    PreparedStatement s = c.prepareStatement(sql);
                    s.setString(1, name);
                    s.setInt(2, id);
                    ResultSet rs = s.executeQuery();
                    if (rs.next()) {
                        sql = "update librarian set password = ? where name = ?";
                        s = c.prepareStatement(sql);
                        s.setString(1, parse);
                        s.setString(2, name);
                        s.executeQuery();
                        JOptionPane.showMessageDialog(null, "password changed succefully","success",0,  new ImageIcon("OIP_2.png"));
                    } else {
                        JOptionPane.showMessageDialog(null, "invalid username or ID","invalid",0,  new ImageIcon("R.png"));
                    }
                    rs.close();
                    s.close();
                    c.close();

                } catch (ClassNotFoundException | SQLException ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(),"invalid",0,new ImageIcon("R.png"));
                }
            } else {
                JOptionPane.showMessageDialog(null, "The two passwords doesn't match","invalid",0,new ImageIcon("R.png"));
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

