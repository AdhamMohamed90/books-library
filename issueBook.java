    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int row = jTable1.getSelectedRow();
                Class.forName("oracle.jdbc.driver.OracleDriver");
                Connection c = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "system", "123");
                String get = "select * from book where name = ?";

                PreparedStatement prs = c.prepareStatement(get);
                prs.setString(1, jTable1.getValueAt(row, 0).toString());
                ResultSet rs = prs.executeQuery();
                while (rs.next()) {
                    String name = rs.getString(1);
                    String creator = rs.getString(2);
                    String department = rs.getString(3);
                    int price = rs.getInt(4);
                    String transfer = "insert into issuebook values(?,?,?,?,sysdate,sysdate+?)";
                    PreparedStatement ps = c.prepareStatement(transfer);
                    ps.setString(1, name);
                    ps.setString(2, creator);
                    ps.setString(3, department);
                    ps.setInt(4, price);
                    ps.setInt(5, Integer.parseInt(jTextField3.getText()));
                    ps.executeUpdate();
                    String sql = "delete from book where name = ?";
                    PreparedStatement s = c.prepareStatement(sql);
                    s.setString(1, jTable1.getValueAt(row, 0).toString());
                    s.executeUpdate();
                    model.removeRow(jTable1.getSelectedRow());
                    JOptionPane.showMessageDialog(null, "book issued succefully", "success", 0, new ImageIcon("OIP_2.png"));

                }

            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(),"invalid",0,new ImageIcon("R.png"));
            }
    }//GEN-LAST:event_jButton1ActionPerformed

